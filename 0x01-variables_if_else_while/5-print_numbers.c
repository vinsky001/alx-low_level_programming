#include <stdio.h>

/**
 * *main - print all single digit numbers of base 10
 * *Description: starting from 0
 * *followed by a new line
 * *all code should be in the main function
 * *Return: 0
 * */

int main(void)
{
		int d;

		for (d = 0 ; d < 10 ; d++)
	{
		printf("%d", d);
}
		printf("\n");
		return (0);
}
